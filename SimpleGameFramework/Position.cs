namespace SimpleGameFramework;

/// <summary>
/// An entity's position given by its xy coordinates.
/// Contained in <see cref="WorldObject"/>, <see cref="Creature"/>
/// </summary>
public class Position
{
    /// <summary>
    /// X coordinate
    /// </summary>
    public int X { get; set; }
    /// <summary>
    /// Y coordinate
    /// </summary>
    public int Y { get; set; }
}